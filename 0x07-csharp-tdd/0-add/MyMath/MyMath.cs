using System;

namespace MyMath
{

    /// <summary>
    ///  Class containing basic operations
    /// </summary>
    public class Operations
    {
        /// <summary>
        ///  Simple function to add two numbers
        /// </summary>
        /// <param name="a"> First paramater to add, can only be int</param>
        /// <param name="b"> Second number to add, can only be int</param>
        /// <returns>A + B</returns>
        public static int Add(int a, int b)
        {
            return(a + b);
        }
    }
}
